<script type="text/javascript">
	$(document).ready(function(){

		$(".navi-link a").on('click', function(event) {

		    if (this.hash !== "") {

		      var hash = this.hash;

		      $('html, body').animate({
		        scrollTop: $(hash).offset().top
		      }, 1000, function() {

		        window.location.hash = hash;
		      });
		      return false;
		    } 

	  });

	  	var owl = $("#owl-testi").owlCarousel({
	  		dots: true,
	  		stagePadding: 50,
	  		items: 2,
	  		responsive : {
	  			480 : {
			        items : 1
			    },
	  			640 : {
			        items : 1
			    },
			    768 : {
			        items : 2
			    }
			}
	  	});

	  	checkClasses();
	    owl.on('translated.owl.carousel', function(event) {
	        checkClasses();
	    });

	    $("#owl-gallery").owlCarousel({
	  		items: 3,
	  		autoWidth:true
	  	});

	});

	$(window).scroll(function(){
	    if ($(window).scrollTop() >= 300) {
	        $('header').addClass('fixed');
	        $('body').addClass('scrolled');
	    }
	    else {
	        $('header').removeClass('fixed');
	        $('body').removeClass('scrolled');
	    }
	});

    function checkClasses(){
        var total = $('#owl-testi .owl-stage .owl-item.active').length;
        
        $('#owl-testi .owl-stage .owl-item').removeClass('itemActive');
        
        $('#owl-testi .owl-stage .owl-item.active').each(function(index){
            if (index === 0) {
                // this is the first one
                $(this).addClass('itemActive');
            }
        });
    }
</script>